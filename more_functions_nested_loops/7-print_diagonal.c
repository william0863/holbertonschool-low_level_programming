#include "main.h"

/**
 * print_diagonal - Print a diagonal n times with _putchar.
 * @n : number of characters to draw.
 * @i : 
 * @b : 
 */

void print_diagonal(int n)
{
int i;
int b;
for (i = n; i <= b; ++i)
{
int j;
for (j = n; j <= i; ++j)
{
_putchar('\\');
}
_putchar('\n');
}
_putchar('\n');
}
